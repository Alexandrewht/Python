adicao = 10 + 10
print('Adição: ', adicao)

subtracao = 10 - 5
print('subtração: ', subtracao)

multiplicacao = 10 * 10
print('Multiplicação: ', multiplicacao)

divisao = 10 / 2.2 # Divisão é sempre float
print('Divisão: ', divisao)

divisao_inteira = 10 // 2.2 # Divisão inteira não retorna casas decimais
print('Divisão inteira: ', divisao_inteira)

exponenciacao = 10 ** 10
print('Exponenciação: ', exponenciacao)

modulo = 25 % 5 # resto da divisão
print('Modulo: ', modulo)

# Exemplos com restos da divisão
print(10 % 8 == 0)
print(16 % 8 == 0)
print(10 % 2 == 0)
print(15 % 2 == 0)
print(16 % 2 == 0)
"""
Python = Linguagem de programação
Tipo de tipagem Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Alexandre :)')
print(1, 'Alexandre ":)"') 

# Aspas duplas
print("Alexandre (:")
print(2, "Alexandre '(:'") 

# Escape -> com o \ pula o fechamento do interpretador
print("Alexandre \" ^^ \"")

# r (expressões regulares) mostra oque o caractere de escape pula sozinho
print(r'Alexandre \" ^^ \"')